<p><h2>European Netrek League</h2>
The ENL is the European counterpart to the INL. There
are no restrictions preventing non-Europeans from playing in the ENL
and Europeans from playing in the INL; in fact, some players
participate in both leagues. But in general, trans-continental Internet
connections tend to be quite bad, discouraging European players from
playing in the INL. The good old days of 100ms pings over the Atlantic
are far gone.

<ul>
	<li>
	<a href="http://enl.netrek.org/">ENL Home Page</a><br />
	The main home page for the ENL with game archives,
	teams, statistics and info on how to join. As of Summer 1999
	the European Netrek League is no longer active (due to too
	many graduations, marriages, RSI, etc.)
	The website remains active as information source and archive.
	</li>
</ul>

