<h1>Tao of Netrek</h1>
<p>
Do you find netrek to be a fun, relaxing way to relieve stress?  No?
Do you get the urge to throttle your teammates when they do dumb
things, or (more usually) fail to do anything useful at all?  Does it
feel like a knife in the gut every time you see the enemy take a
planet?  Do you find the game to be less fun when the opposition is
good?  Do you send team messages, as Red Shirt has been known to do,
like "You incompetent morons!  Shoot that cloaker!"?  Perhaps this post
is for you.
</p>

<p>
A couple weeks ago I started the character Arjuna, named after the
warrior in the Bhagavad-Gita (The Song of God).  The Gita is sort of
the Gospel of Hinduism.  Arjuna is an archer who is supposed to go into
battle, and Krishna is his charioteer (as well as being God).  Krishna
explains to Arjuna a yoga, actually several yogas, which are
precribed paths of spiritual life.  Now I'm not religious, and don't
know much about this stuff, so take this with a grain of salt.  But I
liked the "Karma Yoga", the path of selfless, God-dedicated action.
Here are a few quotes regarding this yoga:
</p>

<ul>
<p>
<li>	Be even-tempered in success and failure.
</li>
</p>

<p>
<li>	Realize that pleasure and pain, gain and loss, victory and
	defeat, are all one and the same:  then go into battle.
</li>
</p>

<p>
<li>	Work done with anxiety about results is far inferior to work
	done without such anxiety, in the calm of self-surrender.
</li>
</p>

<p>
<li>	Control the lusts of your heart, and renounce the fruits of
	every action.
</li>
</p>

<p>
<li>	Your motive in working should be to set others, by your
	example, on the path of duty.
</li>
</p>

<p>
<li>	Stop hoping for wordly rewards.  Fix your mind on the Atman
	[the Godhead within each being].  Be free from the sense of
	ego.  Dedicate all your actions to me [Krishna].  Then go
	forward and fight.
</li>
</p>

<p>
<li>	Prefer to die doing your own duty:  the duty of another will
	bring you into great spiritual danger.
</li>
</p>
</ul>

